a = int(input("Zadejte stranu a="))
b = int(input("Zadejte stranu b="))

print(f"Obsah čtverce se stranou a={a} je S={a**2}")
print(f"Obsah obdélníku se stranou a={a} a b={b} je S={a*b}")

r = int(input("Zadejte poloměr kruhu r="))
print(f"Obsah kruhu s poloměrem r={r} je S={3.14*(r**2)}")
print(f"Obvod kruhu s poloměrem r={r} je o={2*3.14*r}")
